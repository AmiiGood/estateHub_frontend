function AppContent() {
  const { user } = useAuth();
  const [showLogin, setShowLogin] = useState(true);

  if (user) {
    return <Dashboard />;
  }

  return showLogin ? (
    <Login onToggleView={() => setShowLogin(false)} />
  ) : (
    <Register onToggleView={() => setShowLogin(true)} />
  );
}

function App() {
  return (
    <AuthProvider>
      <AppContent />
    </AuthProvider>
  );
}

export default App;
